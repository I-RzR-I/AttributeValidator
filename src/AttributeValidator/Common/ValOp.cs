namespace RzR.Validation.Attributes.Common
{
    /// -------------------------------------------------------------------------------------------------
    /// <summary>
    ///     Comparison operators used by cross-property validation attributes.
    /// </summary>
    /// =================================================================================================
    public enum ValOp
    {
        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        ///     The left operand must equal the right operand.
        /// </summary>
        /// =================================================================================================
        Equals,

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        ///     The left operand must not equal the right operand.
        /// </summary>
        /// =================================================================================================
        NotEquals,

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        ///     The left operand must be strictly greater than the right operand.
        /// </summary>
        /// =================================================================================================
        GreaterThan,

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        ///     The left operand must be greater than or equal to the right operand.
        /// </summary>
        /// =================================================================================================
        GreaterThanOrEqual,

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        ///     The left operand must be strictly less than the right operand.
        /// </summary>
        /// =================================================================================================
        LessThan,

        /// -------------------------------------------------------------------------------------------------
        /// <summary>
        ///     The left operand must be less than or equal to the right operand.
        /// </summary>
        /// =================================================================================================
        LessThanOrEqual
    }
}